package POJO;



/**
 *
 * @author Luong Vinh Khang
 */
public class SanPhamPOJO {
    int maSP;
    String tenSP;
    float donGia;
    int soLuong;
    
    //Các phương thức khởi tạo
    public SanPhamPOJO() {
    }

    public SanPhamPOJO(int maSP, String tenSP, float donGia, int soLuong) {
        this.maSP = maSP;
        this.tenSP = tenSP;
        this.donGia = donGia;
        this.soLuong = soLuong;
    }

    public int getMaSP() {
        return maSP;
    }

    public void setMaSP(int maSP) {
        this.maSP = maSP;
    }

    public String getTenSP() {
        return tenSP;
    }

    public void setTenSP(String tenSP) {
        this.tenSP = tenSP;
    }

    public float getDonGia() {
        return donGia;
    }

    public void setDonGia(float donGia) {
        this.donGia = donGia;
    }

    public int getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(int soLuong) {
        this.soLuong = soLuong;
    }
    
}
