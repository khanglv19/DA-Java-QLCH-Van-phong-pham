    public void hienThi(){
        ArrayList<SanPhamPOJO> dsSP = SanPhamDAO.layDanhSachSanPham();
        dtm.setRowCount(0);
        for(SanPhamPOJO sp: dsSP){
            Vector<Object> vec = new Vector<Object>();
            vec.add(sp.getMaSP());
            vec.add(sp.getTenSP());
            vec.add(sp.getDonGia());
            dtm.addRow(vec);
        }
        tblSanPham.setModel(dtm);
    }

